package com.example.demo.uce.controller.DTO;

public class ReservaDTO {
	
	private Integer numero;
	private Integer asientosCompra;
	private String numTarjeta;
	private String nombre;
	
	public Integer getNumero() {
		return numero;
	}
	public void setNumero(Integer numero) {
		this.numero = numero;
	}
	public Integer getAsientosCompra() {
		return asientosCompra;
	}
	public void setAsientosCompra(Integer asientosCompra) {
		this.asientosCompra = asientosCompra;
	}
	public String getNumTarjeta() {
		return numTarjeta;
	}
	public void setNumTarjeta(String numTarjeta) {
		this.numTarjeta = numTarjeta;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	

}
